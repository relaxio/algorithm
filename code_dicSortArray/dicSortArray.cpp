#include <iostream>
#include <algorithm>

using namespace std;

int main()
{
    int n;
    while(cin >> n)
    {
        int *cc = new int[n + 1];
        int t = 1, j = 1, k = 1;
        t = 1;            
        while(t < n && j < n+1)
        {
            while (t < n) { 
                cc[j] = t;
                t *= 10; ++j;
            }
            t /= 10;
            for (k = t + 1; k <= n && k < ((t/10 + 1)*10); ++k)
            {
                cc[j] = k;
                ++j;
            }
            t /= 10;
            while(9 == t%10) t /= 10;
            t += 1;
        }
        for (int i = 1; i <= n; ++i)
        {
            cout << cc[i] << endl;
        }
        sort(&cc[1], &cc[n+1]);
        for (int i = 1; i <= n; ++i)
        {
            cout << cc[i] << endl;
        }
    }
}

